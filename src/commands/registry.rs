/// const-fn constructor so the table is purely static data — no init cost.
#[allow(clippy::too_many_arguments)]
const fn spec(
    name: &'static str,
    arity: i32,
    flags: &'static [&'static str],
    first_key: i32,
    last_key: i32,
    key_step: i32,
    summary: &'static str,
    group: &'static str,
    since: &'static str,
) -> CommandSpec {
    CommandSpec {
        name,
        arity,
        flags,
        first_key,
        last_key,
        key_step,
        acl_categories: &[], // populated at runtime if we ever need to derive
        summary,
        group,
        since,
    }
}

/// Look up a spec by case-insensitive name.
pub fn lookup(name: &[u8]) -> Option<&'static CommandSpec> {
    // Linear scan: ~150 entries × ~10 ASCII compares = ~1500 cycles, well
    // below FDB latency. A perfect hash isn't worth the build complexity.
    let upper = name.to_ascii_uppercase();
    COMMAND_TABLE.iter().find(|s| s.name.as_bytes() == upper.as_slice())
}

/// True if `name` is a recognized command.
pub fn is_known(name: &[u8]) -> bool {
    lookup(name).is_some()
}

/// Total number of registered commands.
pub fn count() -> usize {
    COMMAND_TABLE.len()
}

/// Static metric label for `name`. Falls back to `"UNKNOWN"` so an
/// attacker can't blow up label cardinality with random command names.
///
/// Returns a `&'static str` so it can be passed to `with_label_values`
/// without allocation on the hot path.
pub fn metric_label(name: &[u8]) -> &'static str {
    match lookup(name) {
        Some(s) => s.name,
        None => "UNKNOWN",
    }
}

/// Render a `CommandSpec` as the RESP array Redis returns from
/// `COMMAND INFO`. Format:
///
/// 1. name (bulk string, lowercase)
/// 2. arity (integer)
/// 3. flags (array of simple strings)
/// 4. first key position (integer)
/// 5. last key position (integer)
/// 6. step (integer)
/// 7. ACL categories (array of simple strings, empty here)
/// 8. tips (array, empty)
/// 9. key specs (array, empty)
/// 10. subcommands (array, empty)
pub fn render_command_info(spec: &CommandSpec) -> RespValue {
    let flags: Vec<RespValue> = spec
        .flags
        .iter()
        .map(|f| RespValue::SimpleString(Bytes::from(*f)))
        .collect();
    let acl: Vec<RespValue> = spec
        .acl_categories
        .iter()
        .map(|f| RespValue::SimpleString(Bytes::from(*f)))
        .collect();
    RespValue::Array(Some(vec![
        RespValue::BulkString(Some(Bytes::from(spec.name.to_ascii_lowercase()))),
        RespValue::Integer(spec.arity as i64),
        RespValue::Array(Some(flags)),
        RespValue::Integer(spec.first_key as i64),
        RespValue::Integer(spec.last_key as i64),
        RespValue::Integer(spec.key_step as i64),
        RespValue::Array(Some(acl)),
        RespValue::Array(Some(vec![])),
        RespValue::Array(Some(vec![])),
        RespValue::Array(Some(vec![])),
    ]))
}

/// Render a `CommandSpec` as the RESP map returned by `COMMAND DOCS`.
pub fn render_command_docs(spec: &CommandSpec) -> RespValue {
    RespValue::Map(vec![
        (
            RespValue::BulkString(Some(Bytes::from_static(b"summary"))),
            RespValue::BulkString(Some(Bytes::from(spec.summary))),
        ),
        (
            RespValue::BulkString(Some(Bytes::from_static(b"since"))),
            RespValue::BulkString(Some(Bytes::from(spec.since))),
        ),
        (
            RespValue::BulkString(Some(Bytes::from_static(b"group"))),
            RespValue::BulkString(Some(Bytes::from(spec.group))),
        ),
        (
            RespValue::BulkString(Some(Bytes::from_static(b"arguments"))),
            RespValue::Array(Some(vec![])),
        ),
    ])
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn lookup_is_case_insensitive() {
        assert!(lookup(b"PING").is_some());
        assert!(lookup(b"ping").is_some());
        assert!(lookup(b"PiNg").is_some());
        assert!(lookup(b"NOTACOMMAND").is_none());
    }

    #[test]
    fn metric_label_unknown_falls_back_to_static() {
        let l = metric_label(b"definitely_not_a_command");
        assert_eq!(l, "UNKNOWN");
    }

    #[test]
    fn count_matches_table_len() {
        assert_eq!(count(), COMMAND_TABLE.len());
        assert!(count() > 100);
    }

    #[test]
    fn no_duplicate_names() {
        // Two duplicate registry entries would silently make
        // is_known/lookup ambiguous. Catch it at compile/test time.
        let mut names: Vec<&'static str> = COMMAND_TABLE.iter().map(|s| s.name).collect();
        names.sort_unstable();
        for w in names.windows(2) {
            assert_ne!(w[0], w[1], "duplicate command in registry: {}", w[0]);
        }
    }

    #[test]
    fn names_are_uppercase() {
        for s in COMMAND_TABLE {
            assert_eq!(
                s.name,
                s.name.to_ascii_uppercase(),
                "command names in the registry must be uppercase"
            );
        }
    }
}
