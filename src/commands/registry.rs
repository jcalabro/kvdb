//! Single source of truth for command metadata.
//!
//! The [`COMMAND_TABLE`] is a static slice of [`CommandSpec`] entries
//! describing every command we support. It powers:
//!
//! - `COMMAND COUNT / DOCS / INFO / LIST` (admin commands clients use to
//!   probe what we support).
//! - `metric_label_for_command()` (bounded-cardinality metric label
//!   whitelist — replaces the hand-rolled match in `connection.rs`).
//! - `is_known_command()` (used by MULTI to decide whether to set the
//!   `EXECABORT` flag).
//! - Future use by `CLUSTER COUNTKEYSINSLOT`-style commands.
//!
//! Adding a new command means adding one row here and one branch in
//! `dispatch()` — the registry guarantees the metric label and
//! command-info responses stay in sync without manual upkeep.

use bytes::Bytes;

use crate::protocol::types::RespValue;

/// Per-command specification. Mirrors the fields Redis exposes via
/// `COMMAND INFO` / `COMMAND DOCS` so client libraries can introspect.
#[derive(Debug, Clone, Copy)]
pub struct CommandSpec {
    /// Uppercase command name (matches the dispatch table).
    pub name: &'static str,
    /// Number of arguments (including the command name itself).
    /// Negative means "at least |arity|" (matching Redis convention).
    pub arity: i32,
    /// Command flags (e.g. "write", "readonly", "fast"). Comma-separated.
    pub flags: &'static [&'static str],
    /// 1-based position of the first key in argv, or 0 if no keys.
    pub first_key: i32,
    /// 1-based position of the last key in argv, or -N to mean "Nth from end".
    pub last_key: i32,
    /// Step between successive keys (1 for SET, 2 for MSET).
    pub key_step: i32,
    /// ACL categories (e.g. "@read", "@write", "@fast"). Comma-separated.
    pub acl_categories: &'static [&'static str],
    /// One-line summary used by `COMMAND DOCS`.
    pub summary: &'static str,
    /// Group ("string", "hash", "set", etc.) used by `COMMAND DOCS`.
    pub group: &'static str,
    /// Version when the command was first introduced — informational.
    pub since: &'static str,
}

